package DataObjects;

public class SituacaoAlerta {
	
	public String id_sit;

	public String titulo_sit;

	public String descricao_sit;

	public String id_seg;

	
	public SituacaoAlerta (String  id_sit, String titulo_sit, String descricao_sit, String id_seg)
	{
		this. id_sit =  id_sit;
	
		this.titulo_sit = titulo_sit;
	
		this.descricao_sit = descricao_sit;
	
		this.id_seg = id_seg;
		
	}

}
