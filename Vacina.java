
public class Vacina {
	
	public String id_vacina;

	public String nome_vac;

	public String descricao_vac;

	public String data_validade;

	public String id_bol;
	
	
	public Vacina (String  id_vacina, String nome_vac, String descricao_vac, String data_validade, String id_bol)
	{
		this. id_vacina =  id_vacina;
	
		this.nome_vac = nome_vac;
	
		this.descricao_vac = descricao_vac;
	
		this.data_validade = data_validade;
		
		this.id_bol = id_bol;
	
}
}